#data types in python

#numeric data types
#intger 
a=12
print(a)
print(type(a))

#float
b=3.987654321
print(b)
print(type(b))

#complex
e=2+3j
print(e)
print(type(e))

#sequence data types
#string
c="devraj"
print(c)
print(type(c))

#list
f=[1,2,3,4,5]
print(f)
print(type(f))

#tuple
g=(1,2,3,4,5)
print(g)
print(type(g))

#dictionary
h={"name":"devraj","age":18}
print(h)
print(type(h))

#set
i={1,2,3,4,5}
print(i)
print(type(i))

#boolean
bool=True
print(bool)
print(type(bool))

#binarydata types
#bytes
j=b"hello"
print(j)
print(type(j))

#bytearray
k=bytearray(b"hello")
print(k)
print(type(k))

#memoryview
l=memoryview(b"hello")
print(l)
print(type(l))
